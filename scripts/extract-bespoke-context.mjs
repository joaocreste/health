#!/usr/bin/env node
/**
 * One-time (re-runnable) extractor: lift the bespoke-patient data out of the
 * browser assets so the Worker-side chat context builder can import it without
 * pulling in browser globals (window/document/location).
 *
 *   web/assets/silvana-labs.js   →  window.SILVANA_LABS
 *   web/assets/patient-context.js →  var PAULO_STUDIES, var SILVANA_INBODY
 *
 * Emits importable ES modules:
 *   lib/patient-context-data/silvana.js  →  default { labs, inbody }
 *   lib/patient-context-data/paulo.js    →  default { studies }
 *
 * The browser assets remain the source of truth (they drive the live UI). Re-run
 * this whenever those curated literals change:  node scripts/extract-bespoke-context.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

/* Walk a balanced [...] / {...} starting at `open`, skipping over string
   literals ('  "  `) and // /* comments so braces inside them aren't counted. */
function matchBalanced(src, open) {
  const closeOf = { "[": "]", "{": "}" };
  const want = closeOf[src[open]];
  if (!want) throw new Error(`expected [ or { at ${open}, got ${src[open]}`);
  let depth = 0;
  for (let i = open; i < src.length; i++) {
    const c = src[i];
    if (c === "/" && src[i + 1] === "/") { i = src.indexOf("\n", i); if (i < 0) i = src.length; continue; }
    if (c === "/" && src[i + 1] === "*") { i = src.indexOf("*/", i + 2) + 1; continue; }
    if (c === "'" || c === '"' || c === "`") {
      const q = c;
      for (i++; i < src.length; i++) { if (src[i] === "\\") i++; else if (src[i] === q) break; }
      continue;
    }
    if (c === "[" || c === "{") depth++;
    else if (c === "]" || c === "}") { depth--; if (depth === 0) return src.slice(open, i + 1); }
  }
  throw new Error("unbalanced literal");
}

function extractLiteral(src, declRe, label) {
  const m = declRe.exec(src);
  if (!m) throw new Error(`could not find ${label}`);
  // declRe matches up to and including `= ` (trailing whitespace), so the
  // literal's opening bracket is the first non-space char after the match.
  let start = m.index + m[0].length;
  while (start < src.length && src[start] !== "[" && src[start] !== "{") start++;
  const literal = matchBalanced(src, start);
  // eslint-disable-next-line no-new-func — one-time local build over trusted repo files
  return new Function(`return (${literal});`)();
}

const silvanaSrc = readFileSync(join(root, "web/assets/silvana-labs.js"), "utf8");
const pcSrc = readFileSync(join(root, "web/assets/patient-context.js"), "utf8");

const SILVANA_LABS = extractLiteral(silvanaSrc, /window\.SILVANA_LABS\s*=\s*/, "window.SILVANA_LABS");
const PAULO_STUDIES = extractLiteral(pcSrc, /var\s+PAULO_STUDIES\s*=\s*/, "PAULO_STUDIES");
const SILVANA_INBODY = extractLiteral(pcSrc, /var\s+SILVANA_INBODY\s*=\s*/, "SILVANA_INBODY");

const outDir = join(root, "lib/patient-context-data");
mkdirSync(outDir, { recursive: true });

const banner = (src) => `/* GENERATED by scripts/extract-bespoke-context.mjs — do not edit by hand.
   Source of truth: ${src}. Re-run the extractor after editing that asset. */\n`;

writeFileSync(
  join(outDir, "silvana.js"),
  banner("web/assets/silvana-labs.js (labs) + web/assets/patient-context.js (inbody)") +
    `export default ${JSON.stringify({ labs: SILVANA_LABS, inbody: SILVANA_INBODY }, null, 2)};\n`,
  "utf8",
);
writeFileSync(
  join(outDir, "paulo.js"),
  banner("web/assets/patient-context.js (PAULO_STUDIES)") +
    `export default ${JSON.stringify({ studies: PAULO_STUDIES }, null, 2)};\n`,
  "utf8",
);

const labMarkers = (SILVANA_LABS.panels || []).reduce((n, p) => n + (p.markers?.length || 0), 0);
console.log(`silvana.js: ${SILVANA_LABS.documents?.length || 0} docs, ${SILVANA_LABS.studies?.length || 0} studies, ` +
  `${SILVANA_LABS.panels?.length || 0} panels / ${labMarkers} markers, InBody ${SILVANA_INBODY.composition?.length || 0} composition rows`);
console.log(`paulo.js: ${PAULO_STUDIES.length} MRI studies`);
